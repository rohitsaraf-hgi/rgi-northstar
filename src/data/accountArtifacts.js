export function getArtifactsForAccount(accountId) {
  const seed = ACCOUNT_ARTIFACTS[accountId] || [];
  return seed.slice().sort((a, b) => {
    const ta = a.generatedAt ? Date.parse(a.generatedAt) : 0;
    const tb = b.generatedAt ? Date.parse(b.generatedAt) : 0;
    return tb - ta;
  });
}

export function getArtifactCountForAccount(accountId) {
  return (ACCOUNT_ARTIFACTS[accountId] || []).length;
}
