// "Added to book" tracking — accounts the rep has converted via the Add-to-book
// flow. Stored in localStorage per persona.
const STORAGE_KEY_PREFIX = 'rgi-added-from-whitespace-';
const CHANGE_EVENT = 'rgi:whitespace-added-changed';

function storageKey(personaId) {
  return `${STORAGE_KEY_PREFIX}${personaId}`;
}

export function listAddedAccountIds(personaId) {
  if (typeof window === 'undefined') return [];
  try {
    const raw = window.localStorage.getItem(storageKey(personaId));
    if (!raw) return [];
    const parsed = JSON.parse(raw);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

export function isAccountAdded(personaId, accountId) {
  return listAddedAccountIds(personaId).includes(accountId);
}

export function markAccountAdded(personaId, accountId) {
  if (typeof window === 'undefined') return;
  const current = listAddedAccountIds(personaId);
  if (current.includes(accountId)) return;
  try {
    window.localStorage.setItem(storageKey(personaId), JSON.stringify([...current, accountId]));
    window.dispatchEvent(new Event(CHANGE_EVENT));
  } catch {
    // ignore quota
  }
}

export function subscribeAdded(onChange) {
  if (typeof window === 'undefined') return () => {};
  const handler = () => onChange();
  window.addEventListener(CHANGE_EVENT, handler);
  return () => window.removeEventListener(CHANGE_EVENT, handler);
}

// Returns whitespace accounts NOT yet added to the rep's book.
export function listAvailableWhitespace(personaId) {
  const added = new Set(listAddedAccountIds(personaId));
  return WHITESPACE_ACCOUNTS.filter((a) => !added.has(a.id));
}

// Returns whitespace accounts that have been added to the rep's book (for the
// "recently added" surface on home and book mode).
export function listAddedFromWhitespace(personaId) {
  const ids = new Set(listAddedAccountIds(personaId));
  return WHITESPACE_ACCOUNTS.filter((a) => ids.has(a.id));
}
