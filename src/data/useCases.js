// Helpers
export function featuredForPersona(personaId) {
  return USE_CASES.filter((u) => u.featuredFor?.includes(personaId));
}

export function relevantForPersona(personaId) {
  // For Maya: marketing-relevant. For Jordan: AE+AM. For Priya: RevOps + relevant ops cases.
  const personaToTags = {
    maya: ['Marketing'],
    jordan: ['AE', 'AM'],
    priya: ['RevOps'],
  };
  const tags = personaToTags[personaId] || [];
  return USE_CASES.filter((u) => u.personas.some((p) => tags.includes(p)));
}
